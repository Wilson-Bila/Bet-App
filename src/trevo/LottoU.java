    private void btnG3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnG3ActionPerformed

        int n1 = Integer.parseInt(lblB1.getText());
        int n2 = Integer.parseInt(lblB2.getText());
        
             int ale =(int) (2 + Math.random()*9-1);
            lblB3.setText(Integer.toString(ale));
            btnG3.setEnabled(false);
            spAp.setEnabled(false);
        
        if (ale==n1 || ale==n2) {
            int ale2 =(int) (2 + Math.random()*5-1);
            lblB3.setText(Integer.toString(ale2));
        }
        else{
            lblB3.setText(Integer.toString(ale));
        }
        
            int b1= Integer.parseInt(lblB1.getText());
            int b2= Integer.parseInt(lblB2.getText());
            int b3= Integer.parseInt(lblB3.getText());
            int t1= Integer.parseInt(txtB2.getText());
            int t2= Integer.parseInt(txtB1.getText());
            int t3= Integer.parseInt(txtB3.getText());
            int sal= Integer.parseInt(lblSal.getText());
            int ap = Integer.parseInt(spAp.getValue().toString());
            lblAp.setText(Integer.toString(ap));
        
        if (b1!=b2 && b1!=b3 && b2!=b3) {
                btnAp.setEnabled(true);    
            if (b1!=t1 && b1!=t2 && b1!=t3 || b2!=t1 && b2!=t2 && b2!=t3 || b3!=t1 && b3!=t2 && b3!=t3) {
                int sub= sal-ap;
                lblSal.setText(Integer.toString(sub));
                JOptionPane.showMessageDialog(this, "HOJE A SUA SORTE ESTÁ RUIM!");
                lblPerd.setText("Valor perdido:");
                lblGanho.setText("Valor ganho:");
                lblGa.setText("0.00");
                lblAd.setVisible(false);
            }
       
            else{
                if(ap>550){
                int ns=ap*12;
                JOptionPane.showMessageDialog(this, "PARABÉNS, VOCÊ GANHOU " +ns+ " METICAIS"); 
                int sub= ns+sal;
                lblSal.setText(Integer.toString(sub));
                lblMul.setVisible(true);
                lblGanho.setText("Valor ganho:");
                lblPerd.setText("Valor perdido:");
                lblSub.setText("0.00");
                lblSin.setVisible(false);
                lblMul.setText("x12");
                
                }
                else{
                int ns=ap*8;
                JOptionPane.showMessageDialog(this, "PARABÉNS, VOCÊ GANHOU " +ns+ " METICAIS"); 
                int sub= ns+sal;
                lblSal.setText(Integer.toString(sub));
                lblMul.setVisible(true);
                lblGanho.setText("Valor ganho:");
                lblPerd.setText("Valor perdido:");
                lblSub.setText("0.00");
                lblSin.setVisible(false);
                
                }
                }
            }  
        
        else{
           JOptionPane.showMessageDialog(this, "Houve um erro...\n Por favor, gire novamente!", "", JOptionPane.ERROR_MESSAGE);  
           btnG1.setEnabled(true);  
           btnAp.setEnabled(false);
                } 
            
    }//GEN-LAST:event_btnG3ActionPerformed

