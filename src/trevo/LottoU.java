    private void btnConfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfActionPerformed
        try {
        
        int ap1 = Integer.parseInt(txtB2.getText());
        int ap2 = Integer.parseInt(txtB1.getText());
        int ap3 = Integer.parseInt(txtB3.getText());
        int sal= Integer.parseInt(lblSal.getText());
        int ap = Integer.parseInt(spAp.getValue().toString());

            if (sal>=ap) { 
                
                if (ap1>=1 && ap1<=9 && ap2>=1 && ap2<=9 && ap3>=1 && ap3<=9) {
                    
                    if (ap1!=ap2 && ap1!=ap3 && ap2!=ap3) {
                        btnG1.setEnabled(true);   
                        lblAp.setText(Integer.toString(ap));
                        btnConf.setEnabled(false);
                        txtB2.setEditable(false);
                        txtB1.setEditable(false);
                        txtB3.setEditable(false);
                        spAp.setEnabled(false);
                        
                        if(ap>550){
                        int arp = Integer.parseInt(spAp.getValue().toString());
                        int nqs=arp*12;
                        lblGa.setText(Integer.toString(nqs));
                        lblSub.setText(Integer.toString(ap));
                        lblSin.setVisible(true);
                        lblAd.setVisible(true);
                        }
                        else{
                        int arp = Integer.parseInt(spAp.getValue().toString());
                        int nqs=arp*8;
                        lblGa.setText(Integer.toString(nqs));
                        lblSub.setText(Integer.toString(ap));
                        lblSin.setVisible(true);
                        lblAd.setVisible(true);
                        }
                    } 
                    else{
                       JOptionPane.showMessageDialog(this, "Não repita os números", "", JOptionPane.ERROR_MESSAGE);  
                    }
                     
                }
                else{
                    JOptionPane.showMessageDialog(this, "Números inválidos! \nDigite números no intervalo de 1 a 9", "", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            else{
            JOptionPane.showMessageDialog(this, "Saldo insuficiente!", "", JOptionPane.WARNING_MESSAGE);
            }
            if (sal<20) { 
            int option = JOptionPane.showConfirmDialog(this, "Deseja reiniciar?", "", JOptionPane.YES_NO_OPTION);
            int num=1000;
            switch(option){
                case 0: lblSal.setText(Integer.toString(num)); break;
            }
            spAp.setValue(20);
            txtB2.setText("");
            txtB1.setText("");
            txtB3.setText("");}
            
        } catch (HeadlessException | NumberFormatException erro) {
            JOptionPane.showMessageDialog( this, "Preencha devidamente!", "", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnConfActionPerformed

